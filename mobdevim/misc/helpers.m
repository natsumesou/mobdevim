char* dcolor(char *color) {
  static BOOL useColor = NO;
  
  static dispatch_once_t onceToken;
  dispatch_once(&onceToken, ^{
    if (getenv("DSCOLOR")) {
      useColor = YES;
    }
  });
  if (!useColor) {
    return "";
  }
  if (strcmp("cyan", color) == 0) {
    return "\e[36m";
  } else if (strcmp("yellow", color) == 0) {
    return "\e[33m";
  } else if (strcmp("magenta", color) == 0) {
    return "\e[95m";
  } else if (strcmp("red", color) == 0) {
    return "\e[91m";
  } else if (strcmp("blue", color) == 0) {
    return "\e[34m";
  } else if (strcmp("gray", color) == 0) {
    return "\e[90m";
  } else if (strcmp("bold", color) == 0) {
    return "\e[1m";
  }
  return "";
}

char *colorEnd() {
  static BOOL useColor = NO;
  static dispatch_once_t onceToken;
  dispatch_once(&onceToken, ^{
    if (getenv("DSCOLOR")) {
      useColor = YES;
    }
  });
  if (useColor) {
    return "\e[0m";
  }
  
  return "";
}

void dsprintf(FILE * f, const char *format, ...) {
  if (quiet_mode) {
    return;
  }
  va_list args;
  va_start( args, format );
  vfprintf(f, format, args );
  va_end( args );
}

void dsdebug(const char *format, ...) {
    if (quiet_mode) { return; }
    
    static dispatch_once_t onceToken;
    static BOOL debugFlag = 0;
    dispatch_once(&onceToken, ^{
        if (getenv("DSDEBUG")) {
            debugFlag = YES;
        } else {
            debugFlag = NO;
        }
    });
    
    if (debugFlag) {
        va_list args;
        va_start( args, format);
        vfprintf(stdout, format, args );
        va_end( args );
    }
    
}


void ErrorMessageThenDie(const char *message, ...) {
    if (quiet_mode) {
        return;
    }
    va_list args;
    va_start(args, message);
    vfprintf(stderr, message, args);
    va_end( args );
    exit(1);
}


